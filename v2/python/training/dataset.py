"""
Dataset loader for self-play binary data generated by the Rust engine.

Binary format per sample (316 bytes):
  features: [f32; 70]  - 280 bytes (little-endian)
  policy:   [f32; 9]   - 36 bytes (little-endian)
  value:    f32         - 4 bytes (little-endian)
"""

import struct
import numpy as np
import torch
from torch.utils.data import Dataset

FEATURE_SIZE = 70
NUM_ACTIONS = 9
SAMPLE_SIZE = (FEATURE_SIZE + NUM_ACTIONS + 1) * 4  # 316 bytes


class SelfPlayDataset(Dataset):
    """Load self-play training data from binary file."""

    def __init__(self, path, max_samples=None):
        """
        Args:
            path: Path to binary data file
            max_samples: Optional cap on number of samples to load
        """
        with open(path, "rb") as f:
            raw = f.read()

        total_samples = len(raw) // SAMPLE_SIZE
        if max_samples is not None:
            total_samples = min(total_samples, max_samples)

        # Parse all samples at once using numpy for speed
        data = np.frombuffer(raw[: total_samples * SAMPLE_SIZE], dtype=np.float32)
        data = data.reshape(total_samples, FEATURE_SIZE + NUM_ACTIONS + 1)

        self.features = torch.from_numpy(data[:, :FEATURE_SIZE].copy())
        self.policies = torch.from_numpy(
            data[:, FEATURE_SIZE : FEATURE_SIZE + NUM_ACTIONS].copy()
        )
        self.values = torch.from_numpy(data[:, -1].copy())

        print(f"Loaded {total_samples} samples from {path}")

    def __len__(self):
        return len(self.features)

    def __getitem__(self, idx):
        return self.features[idx], self.policies[idx], self.values[idx]


class MultiFileDataset(Dataset):
    """Load self-play data from multiple generation files with optional window."""

    def __init__(self, paths, window=None):
        """
        Args:
            paths: List of binary data file paths
            window: If set, only use the last `window` files
        """
        if window is not None:
            paths = paths[-window:]

        all_features = []
        all_policies = []
        all_values = []

        for path in paths:
            with open(path, "rb") as f:
                raw = f.read()

            total_samples = len(raw) // SAMPLE_SIZE
            if total_samples == 0:
                continue

            data = np.frombuffer(raw[: total_samples * SAMPLE_SIZE], dtype=np.float32)
            data = data.reshape(total_samples, FEATURE_SIZE + NUM_ACTIONS + 1)

            all_features.append(data[:, :FEATURE_SIZE])
            all_policies.append(data[:, FEATURE_SIZE : FEATURE_SIZE + NUM_ACTIONS])
            all_values.append(data[:, -1])

        features = np.concatenate(all_features, axis=0)
        policies = np.concatenate(all_policies, axis=0)
        values = np.concatenate(all_values, axis=0)

        self.features = torch.from_numpy(features.copy())
        self.policies = torch.from_numpy(policies.copy())
        self.values = torch.from_numpy(values.copy())

        print(f"Loaded {len(self.features)} total samples from {len(paths)} files")

    def __len__(self):
        return len(self.features)

    def __getitem__(self, idx):
        return self.features[idx], self.policies[idx], self.values[idx]


if __name__ == "__main__":
    import sys

    if len(sys.argv) < 2:
        print(f"Usage: python dataset.py <data.bin>")
        sys.exit(1)

    ds = SelfPlayDataset(sys.argv[1])
    print(f"Samples: {len(ds)}")

    if len(ds) > 0:
        feat, pol, val = ds[0]
        print(f"Feature shape: {feat.shape}")
        print(f"Policy shape: {pol.shape}")
        print(f"Value: {val.item():.4f}")
        print(f"Policy sum: {pol.sum().item():.4f}")
